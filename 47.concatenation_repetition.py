t1 = (10, 20) # Create a tuple t1 with the elements 10 and 20
t2 = (11,22,33) # Create a tuple t2 with the elements 11, 22, and 33

# Concatenation
t3 = t1 + t2 # Concatenation of two tuples t1 and t2, which will create a new tuple t3 that contains all the elements of t1 followed by all the elements of t2. The resulting tuple t3 will have the value (10, 20, 11, 22, 33).
print(t3) # Print the concatenated tuple t3 to the console, which should contain the value (10, 20, 11, 22, 33) after the concatenation.

# Repetition
print(t1*3) # Repetition of the tuple t1 three times, which will create a new tuple that contains the elements of t1 repeated three times. The resulting tuple will have the value (10, 20, 10, 20, 10, 20).