s1 = "ABC" # Create a string s1 with the value "ABC"
s2 = "DE" # Create a string s2 with the value "DE"
# Concatenation
s3 = s1 + s2 # Concatenation of two strings
print(s3) # Print the concatenated string s3 to the console, which should contain the value "ABCDE" after the concatenation.

# Repetition
s4 = s1 * 3 # this will show the string s1 repeated 3 times and the output will be "ABCABCABC"
s5 = s2 * 4 # this will show the string s2 repeated 4 times and the output will be "DEDEDEDE"
print(s4) # Print the repeated string s4 to the console

print(s5) # Print the repeated string s5 to the console