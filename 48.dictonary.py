d1 = {102: 'rahul', 105: 'payal', 106: 'arjun', 107: 'prachi'}

#delete a key value pair from dict

del d1[102] #deletes the key value pair with key 102 from dict d1
print(d1) #prints the dict d1 after deleting the key value pair with key 102

#built in functions for dict

print(len(d1)) #number of key value pairs in dict

print(min(d1)) #minimum key in dict

print(max(d1)) #maximum key in dict

print(sum(d1)) #sum of all keys in dict

print(sorted(d1)) #sorted list of keys in dict
