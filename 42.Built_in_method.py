s1 = "Ramesh" # Create a string s1 with the value "Ramesh"
l = [1, 2, 3, 4, 5] # Create a list l with the elements 1, 2, 3, 4, and 5

length_s1 = len(s1) # length of string
length_l = len(l) # length of list
print(length_s1) # length of string
print(length_l) # length of list

min_s1 = min(s1) # minimum character in string
min_l = min(l) # minimum element in list
print(min_s1) # minimum character in string
print(min_l) # minimum element in list

max_s1 = max(s1) # maximum character in string
max_l = max(l) # maximum element in list
print(max_s1) # maximum character in string
print(max_l) # maximum element in list

sum_l = sum(l) # sum of elements in list
print(sum_l) # sum of elements in list

sorted_s1 = sorted(s1) # sorted characters in string
print(sorted_s1) # sorted characters in string