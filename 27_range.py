a = range(1,10,1) # Create a range object a that starts from 1, ends before 10, and increments by 1
for i in a: # Start a for loop that iterates over each number in the range a
    print(i) # Inside the loop, print the current number (i) being iterated over
print("\n") # Print a newline character to separate the output of the first loop from the second loop

