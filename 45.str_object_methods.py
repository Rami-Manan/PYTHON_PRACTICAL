s1 = "Ramesh"# Create a string s1 with the value "Ramesh"

# String Methods
print(s1.index("a"))  # Find the index of 'a'
print(s1.count("a"))  # Count occurrences of 'a'
print(s1.startswith("Ra"))  # Check if the string starts with 'Ra'
print(s1.endswith("sh"))  # Check if the string ends with 'sh'
print(s1.split())  # Split the string into a list of substrings 
print(s1.replace("a", "o"))  # Replace 'a' with 'o'
print(s1.upper())  # Convert to uppercase
print(s1.lower())  # Convert to lowercase
print(s1.format("Ramesh ,{}"))  # Format the string with a placeholder
print(s1.join(["Hello", "World"]))  # Join a list of strings with 's1' as the separator
print(s1.isdigit())  # Check if the string contains only digits
print(s1.islower())  # Check if the string is in lowercase
print(s1.isupper())  # Check if the string is in uppercase
