s1 = "ABC" # Create a string s1 with the value "ABC"
s2 = "DE" # Create a string s2 with the value "DE"

# Comparison
print(s1 < s2)   # This will compare the two strings s1 and s2 and print True if s1 is less than s2, otherwise it will print False. In this case, since "ABC" is less than "DE" in lexicographical order, it will print True.
print(s1 > s2)   # This will compare the two strings s1 and s2 and print True if s1 is greater than s2, otherwise it will print False. In this case, since "ABC" is not greater than "DE" in lexicographical order, it will print False.