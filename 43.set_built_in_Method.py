s1 = {60, 30, 40, 20, 10, 70, 90, 50, 80, 100} # set of integers.

print(len(s1)) # prints the number of elements in the set
print(max(s1)) # prints the maximum value in the set
print(min(s1)) # prints the minimum value in the set
print(sum(s1)) # prints the sum of all elements in the set
print(sorted(s1)) # prints a list of all elements in the set, sorted in ascending order
